Places = ["Palawan, Philippines", "Japan", "Netherlands", "Venice, Italy", "Switzerland"]

sorted_list = sorted(Places)
sorted_list_reverse = sorted(Places, reverse=True)

#displays the original list
print("Original list\n",Places, "\n")

#sorts the list
print("Sorted list\n" ,sorted_list, "\n")

#displays the original list
print("Original list\n",Places, "\n")

#sorts the list in reverse
print("Sorted list in reverse\n" ,sorted_list_reverse, "\n")

#displays the original list
print("Original list\n",Places, "\n")

#reverses the list
Places.reverse()
print("Changing order of list\n",Places, "\n")

#reverses the list again
Places.reverse()
print("Changing order of list again\n",Places, "\n")

#sorts the list
print("Sorted list\n" ,sorted_list, "\n")

#sorts the list in reverse
print("Sorted list in reverse\n" ,sorted_list_reverse, "\n")